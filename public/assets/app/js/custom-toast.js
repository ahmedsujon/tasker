function errorMsg(message) {
    new Notify({
        status: 'error',
        title: '',
        text: message,
        effect: 'fade',
        speed: 300,
        customClass: '',
        customIcon: '',
        showIcon: false,
        showCloseButton: false,
        autoclose: true,
        autotimeout: 3000,
        notificationsGap: null,
        notificationsPadding: null,
        type: 'filled',
        position: 'x-center bottom',
        customWrapper: '',
    })
}

